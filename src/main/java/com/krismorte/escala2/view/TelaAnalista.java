    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um nome e esolha os hor√°rios");
        } else {
            try {
                Analista analista = new Analista();
                analista.setNome(txtNome.getText());
                Equipe eqp = null;
                for (Equipe equipe : equipes) {
                    if (equipe.getNome().equals(boxEquipe.getSelectedItem().toString())) {
                        eqp = equipe;
                        break;
                    }
                }
                analista.setEquipe(eqp);
                if (radSenior.isSelected()) {
                    analista.setNivel(Nivel.Senior);
                } else {
                    analista.setNivel(Nivel.Pleno);
                }
                crudService.salvarAnalista(analista);
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
                txtNome.setText("");
                analistas = crudService.listarAnalistas();
                exibeTabela();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void exibeTabela() {
        BeanTableModel<Analista> modelo = new BeanTableModel<>(Analista.class);
        modelo.addColumn("nome", "nome", BeanTableModel.EditMode.EDITABLE);
        modelo.addColumn("equipe", "equipe");
        modelo.addColumn("Active", "active");

        for (Analista m : analistas) {
            modelo.addRow(m);
        }

        tabelaAnalistas = new MyJTable(modelo);
        //tabelaHorarios.addMouseListener(new MouseEventMenu(menuDirect));
        panelAnalistas.removeAll();
        panelAnalistas.setLayout(new GridLayout(1, 1));

        //tabela = reTweetService.getJTable(listaTweets);
        panelAnalistas.add(ScreenUtil.getJScroll(tabelaAnalistas), BorderLayout.CENTER);
        panelAnalistas.validate();
        //txtTotal.setText("" + horarios.size());
    }

